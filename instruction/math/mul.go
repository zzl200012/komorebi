// Multiply double
type DMUL struct{ base.NoOperandsInstruction }

func (instruction *DMUL) Execute(frame *runtime.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopDouble()
	v1 := stack.PopDouble()
	result := v1 * v2
	stack.PushDouble(result)
}

